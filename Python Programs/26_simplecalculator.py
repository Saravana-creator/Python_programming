a=int(input("Enter number 1 :"))
b=int(input("Enter number 2 :"))
ope=input("Enter an operator :")
if(ope=='+'):
    print("Result :",a+b)
elif(ope=='-'):
    print("Result :",a-b)
elif(ope=='*'):
    print("Result :",a*b)
elif(ope=='/'):
    print("Result :",a/b)
else:
    print("Enter an correct answer")                